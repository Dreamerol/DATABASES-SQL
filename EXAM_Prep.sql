SELECT C.COUNTRY, COALESCE(COUNT(DISTINCT B.NAME), 0) FROM CLASSES C
LEFT OUTER JOIN SHIPS S ON C.CLASS = S.CLASS
LEFT OUTER JOIN OUTCOMES O ON S.NAME = O.SHIP
LEFT OUTER JOIN BATTLES B ON O.BATTLE = B.NAME
GROUP BY C.COUNTRY

