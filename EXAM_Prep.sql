CREATE VIEW battles_cntrs AS
SELECT B.NAME, C.COUNTRY FROM BATTLES B
INNER JOIN OUTCOMES O ON O.BATTLE = B.NAME
INNER JOIN SHIPS S ON S.NAME = O.SHIP
INNER JOIN CLASSES C ON C.CLASS = S.CLASS
GO


SELECT DISTINCT A.NAME FROM battles_cntrs A
WHERE NOT EXISTS (
SELECT COUNTRY FROM battles_cntrs
WHERE NAME = A.NAME
EXCEPT
SELECT COUNTRY FROM battles_cntrs
WHERE NAME = 'Guadalcanal'

)
AND NOT EXISTS(
SELECT COUNTRY FROM battles_cntrs
WHERE NAME = 'Guadalcanal'
EXCEPT
SELECT COUNTRY FROM battles_cntrs
WHERE NAME = A.NAME
)
AND A.NAME != 'Guadalcanal'

