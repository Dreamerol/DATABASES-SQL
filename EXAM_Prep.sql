CREATE TABLE CUSTOMER(
CUSTOMER_ID INT IDENTITY(1,1) PRIMARY KEY,
CUSTOMER_NAME VARCHAR(100),
CUSTOMER_ADDRESS VARCHAR(100),
CUSTOMER_CITY VARCHAR(100),
CITY_CODE VARCHAR(5),
);

CREATE TABLE ORDER_T(
ORDER_ID INT PRIMARY KEY,
ORDER_DATE DATE,
CUSTOMER_ID INT CONSTRAINT FK_CUSTOMER
REFERENCES CUSTOMER(CUSTOMER_ID)
);


CREATE TABLE PRODUCT(
PRODUCT_ID INT PRIMARY KEY,
PRODUCT_DESCREPTION VARCHAR(100),
PRODUCT_FINISH  VARCHAR(100),
STANDART_PRICE FLOAT,
PRODUCT_LINE_ID INT,

CONSTRAINT CHK_FINISH
CHECK (
PRODUCT_FINISH IN (
'череша',
'естествен ясен',
'бял ясен',
'червен дъб', 
'естествен дъб',
'орех'
)
)
);


CREATE TABLE ORDER_LINE(
ORDER_ID INT CONSTRAINT FK_ORDERT
REFERENCES ORDER_T(ORDER_ID),
PRODUCT_ID INT CONSTRAINT FK_PR
REFERENCES PRODUCT(PRODUCT_ID),
ORDERED_QUANTITY INT
);

--2
SELECT P.PRODUCT_ID, P.PRODUCT_DESCREPTION , COUNT(*) AS ORDER_NUMBER FROM PRODUCT P
INNER JOIN ORDER_LINE O ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.PRODUCT_DESCREPTION

--3
SELECT  P.PRODUCT_ID, P.PRODUCT_DESCREPTION ,COALESCE(SUM(O.ORDERED_QUANTITY), 0) FROM PRODUCT P
LEFT OUTER JOIN ORDER_LINE O ON O.PRODUCT_ID = P.PRODUCT_ID
GROUP BY P.PRODUCT_ID, P.PRODUCT_DESCREPTION

--4
SELECT C.CUSTOMER_NAME , SUM(O.ORDERED_QUANTITY * P.STANDART_PRICE) AS TOTAL_PRICE FROM CUSTOMER C
INNER JOIN ORDER_T T ON T.CUSTOMER_ID = C.CUSTOMER_ID
INNER JOIN ORDER_LINE O ON O.ORDER_ID = T.ORDER_ID
INNER JOIN PRODUCT P ON P.PRODUCT_ID = O.PRODUCT_ID
GROUP BY C.CUSTOMER_NAME


