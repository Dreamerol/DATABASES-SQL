SELECT AGENCY, FLIGHT_NUMBER, COUNT(CUSTOMER_ID) AS CUSTOMER_COUNT FROM BOOKINGS
WHERE STATUS = 1
GROUP BY AGENCY, FLIGHT_NUMBER

