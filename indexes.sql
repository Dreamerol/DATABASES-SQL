CREATE VIEW SOFIA_AGENCIES AS
SELECT * FROM AGENCIES
WHERE CITY = 'Sofia'
WITH CHECK OPTION;

--4
CREATE VIEW AGENCIES_NO_PHONE AS
SELECT * FROM AGENCIES
WHERE PHONE IS NULL
WITH CHECK OPTION
--with check option we cannot insert/update data that
--doesnt satisfy this condition

