SELECT C.CLASS, COUNT(SHIP) AS COUNTSUNK FROM CLASSES C
INNER JOIN SHIPS S ON S.CLASS = C.CLASS
INNER JOIN OUTCOMES O ON O.SHIP = S.NAME
WHERE RESULT = 'sunk'
GROUP BY C.CLASS

