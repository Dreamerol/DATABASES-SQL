SELECT T.CLASS, COUNT(SHIP) AS NO_SUNK FROM (SELECT CLASS FROM CLASSES C
WHERE C.CLASS IN (SELECT C1.CLASS FROM
CLASSES C1 
INNER JOIN SHIPS S1 ON S1.CLASS = C1.CLASS
GROUP BY C1.CLASS
HAVING COUNT(S1.NAME) > 2) ) T
INNER JOIN SHIPS S ON S.CLASS = T.CLASS
INNER JOIN OUTCOMES O ON O.SHIP = S.NAME
WHERE O.RESULT = 'sunk'
GROUP BY T.CLASS


